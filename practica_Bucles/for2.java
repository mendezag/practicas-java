/*Ejemplo de uso de while. Programa Java que muestre 
los números del 100 al 1 utilizando la instrucción for*/
package practicas;

public class for2 {
    public static void main(String[] args) {
        int i = 100;
        
        for(i = 100; i>=0;i--){
            System.out.println(i);
        }
    }
}
