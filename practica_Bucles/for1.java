/*Ejemplo de uso de for. Programa Java que muestre los números del 1 al 100
utilizando la instrucción for. */
package practicas;
public class for1 {
    public static void main(String[] args) {
        int i = 0;
        
        for(i=0;i<=100;i++){
            System.out.println(i);
        }
    }
}
